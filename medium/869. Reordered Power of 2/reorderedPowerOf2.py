class Solution(object):
    def reorderedPowerOf2(self, n):
        table = [["1", "2", "4", "8"], ["16", "23", "46"], ["128", "256", "125"], ["0124", "0248", "0469", "1289"], ["13468", "23678", "35566"], ["011237", "122446", "224588"], ["0145678", "0122579", "0134449", "0368888"], ["11266777", "23334455", "01466788"], ["112234778", "234455668", "012356789"], ["0112344778"]]
        num = []
        while n > 0:
            num.append(n % 10)
            n = n // 10
        num.sort()
        s = "".join(str(x) for x in num)
        for a in table[len(num) - 1]:
            if s == a:
                return True
        return False